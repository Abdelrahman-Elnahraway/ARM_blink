// Enable global interrupt using assembly command CPSIE
void inline INTCRL_Enable_globalInterrupts(void)
{
    __asm  ("    CPSIE  I\n");
	
}


// Disable global interrupt using assembly command CPSIE
void inline INTCRL_Disable_globalInterrupts(void)
{
 __asm  ("    CPSID  I\n");
	
}
